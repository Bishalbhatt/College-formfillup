<div class="about jumbotron">
    <h4>About College</h4>
    <p>Pragjyotish College was established on 1st September 1954, seven years after Independence, it became a 
        beacon of learning and a symbol of aspirations for the common people of Assam, rating to build a new nation. 
        Pandit TirthanathSarma, eminent scholar and litterateur, responded to and actively participated in the nation building by taking charge as the founder principal of Pragjyotish College.

From its modest inception as an arts college, Pragjyotish College has now developed into one of the premier institutions of higher education in Gauhati. At present, 
it is a well known full fledged degree college imparting higher education in Arts, Science and Commerce streams. 
The college has also two post graduate department’s viz. Assamese and Tourism Management. It is affiliated to Gauhati University and registered under 2(F) and 12(B) of the University Grants Commission Act, 1956.
 The College has the distinction of being assessed and accredited twice by the NAAC in 2003 and 2010 respectively. Preparations are afoot for undertaking the third-cycle of NAAC accreditation </p>
<p>Situated on the bank of Bharalu, a rivulet, in the western part of Guwahati, Pragjyotish College 
    is about 1 Km away from its confluence with the mighty Brahmaputra and about 4 kmsfrom the Gauhati Railway Station and at a distance of about 20 kms from the LGBI Airport. At the backdrop of the College is a beautiful panoramic view of Nilachal hill, the famous abode o
    f Mother Goddess Kamakhya. At the entrance of the College, the two beautiful ponds present a serene atmosphere for pursuing knowledge and excellence.

In the Emblem of the College, is ingrained the motto ‘Tejasvinavadhitamastu’ (Let our studies be vigorous……..) and a conch shell at the centre on a eight-petaled full-blown beautiful-lotus, which symbolizes pure knowledge and the relationship between the teacher and the learner, praying to Almighty God for energy, protection, maintenance that ultimately leads to peace and bliss.

In its glorious existence of almost sixty four years, Pragjyotish College, as alma mater, has produced a galaxy of emi
nent persons in all walks of life. The College celebrated its Diamond Jubilee Year (2013-14).</p>
